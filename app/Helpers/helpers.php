}

function formatMoney(int $amount): string
{
    if ($amount >= 1000000000) {
        return round($amount / 1000000000, 1) . 'b';
    } elseif ($amount >= 1000000) {
        return round($amount / 1000000, 1) . 'm';
    } elseif ($amount >= 1000) {
        return round($amount / 1000, 1) . 'k';
    } else {
        return $amount;
    }

}


function getCountries()
{
    $countries = Http::get('https://restcountries.com/v3.1/all?fields=name,idd,flags,currencies,capital');
    return $countries;
}

function redirectback()
{
    return redirect()->back();
}

function currentUserPermissions(): array | null
{
    $permissions = \App\Models\Permission::select('name')
        ->whereRelation('roles', 'name', auth()->user()->current_role)
        ->orWhereRelation('users', 'id', Auth::id())
        ->pluck('name')
        ->toArray();
    return $permissions;
}

function statusColor($status)
{
    // $color = "bg-gray-500 text-gray-50";
    switch ($status) {
        case 'active':
            $color = "border-green-500 text-green-500";
            break;
        case 'created':
            $color = "border-green-300 text-green-300";
            break;
        case 'accepted':
            $color = "border-green-500 text-green-500";
            break;
        case 'onLeave':
            $color = "border-orange-400 text-orange-400";
            break;
        case 'credit':
            $color = "border-green-500 text-green-500";
            break;
        case 'debit':
            $color = "border-red-500 text-red-500";
            break;
        case 'expired':
            $color = "border-red-500 text-red-500";
            break;
        case 'expiring':
            $color = "border-yellow-500 text-yellow-500";
            break;
        case 'graduated':
            $color = "border-red-900 text-red-900";
            break;
        default:
            $color = "bg-gray-500 text-gray-500 font-medium";
            break;
    }

    return $color;
}


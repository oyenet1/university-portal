function userNameAbbr($pre): string
{
    if ($pre == "superadmin") {
        return "SAD";
    } else if ($pre == "admin") {
        return "ADM";
    } else if ($pre == "dean") {
        return "DEAN";
    } else if ($pre == "hod") {
        return "HOD";
    } else if ($pre == "lecturer") {
        return "LEC";
    } else {
        return "STU";
    }
}

function getUsersIdsByRole($val): array
{
    $ids = \App\Models\User::select('id')->where('current_role_id', $val)->pluck('id')->toArray();
    return $ids;
}
function getUsersParkIdsByRole($val): array
{
    $ids = \App\Models\User::select('park_id')->where('current_role_id', $val)->pluck('park_id')->toArray();
    return $ids;
}

function getUsersByRole($val): array | object
{
    $users = \App\Models\User::select('id', 'name')->where('current_role_id', $val)->get();
    return $users;
}

function isTimeToRenew($com)
{
    // $date1 = now()->format('Y-m-d');
    $date2 = date_create($com);
    $diff = date_diff(now(), $date2);
    return intval($diff->format("%R%a"));
}

function profilePicture(object $user): string
{
    return $user->avatar ? asset('storage/' . $user->avatar) : asset('img/avatar.png');
}

